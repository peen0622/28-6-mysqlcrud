package service;

public class EmployScore {
	private int employScoreNo;
	private int employNo;
	private int score;
	public int getEmployScoreNo() {
		return employScoreNo;
	}
	public void setEmployScoreNo(int employScoreNo) {
		this.employScoreNo = employScoreNo;
	}
	public int getEmployNo() {
		return employNo;
	}
	public void setEmployNo(int employNo) {
		this.employNo = employNo;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
}
